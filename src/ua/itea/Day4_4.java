package ua.itea;

public class Day4_4 {
	public static void main(String[] args) {
		int a = 5;
		switch (a) {
		case 0:
			System.out.println("Zero");
			break;
		case 1:
			System.out.println("One");
			break;
		case 2:
			System.out.println("Two");
			break;
		default:
			System.out.println("Wrong data");
		}

	}

}
